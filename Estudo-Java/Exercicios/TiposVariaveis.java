package Exercicios;

public class TiposVariaveis {
    public static void main(String[] args) {
        // Tipos primitivos em Java

        // Inteiro (int) - para números inteiros
        int idade = 25;
        System.out.println("Idade: " + idade);

        // Ponto flutuante (double) - para números decimais
        double altura = 1.75;
        System.out.println("Altura: " + altura + " metros");

        // Caractere (char) - para um único caractere
        char inicial = 'Z';
        System.out.println("Inicial: " + inicial);

        // Booleano (boolean) - para verdadeiro/falso
        boolean estudante = true;
        System.out.println("É estudante? " + estudante);

        // Long - para inteiros maiores
        long populacao = 7800000000L;
        System.out.println("População mundial: " + populacao);

        // Float - para decimais com menos precisão
        float temperatura = 25.5f;
        System.out.println("Temperatura: " + temperatura + "°C");

        // Byte - para pequenos inteiros
        byte nota = 10;
        System.out.println("Nota: " + nota);

        // Short - para inteiros curtos
        short ano = 2025;
        System.out.println("Ano: " + ano);
    }
}